def plus(val1, val2):
    return val1 + val2

def minus(val1, val2):
    return val1 - val2

def multiple(val1, val2):
    print(val1 * val2)

def divide(val1, val2):
    print(val1 / val2)

re1 = plus(10, 20)    # 30
re2 = minus(10, 20)   # -10
print(re1, re2)
multiple(re1, re2)    # 200

# 30 multiple -10 = -300